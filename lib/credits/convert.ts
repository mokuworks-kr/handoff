/**
 * USD ↔ 크레딧 환산.
 *
 * ─────────────────────────────────────────────────────────────
 * 임시 정책 (M3a-3-2 ~ M3 검증 종료까지)
 * ─────────────────────────────────────────────────────────────
 *
 * **$0.01 = 1 크레딧, 최소 1**
 *
 * 이 비율은 의도적으로 후함. 검증 단계에서 사용자 부담을 0에 가깝게 두고
 * 실제 사용 데이터를 모은 뒤 진짜 정책으로 교체할 예정.
 *
 * 검증 단계 실측 (M3a-2):
 *   - 분류 1회 ($0.0016 ~ $0.0073) → 1 크레딧 (모두 0.01 미만이라 최소값 적용)
 *   - 신규 가입 1,000 크레딧 → 분류 1,000회 가능
 *
 * 페이지네이션(M3b)·자연어 편집(M3c) 비용 측정 후 진짜 정책 결정.
 * 그 시점에 이 함수만 바꾸면 됨 — credit_transactions.raw_cost_usd 가
 * 진짜 SoT라 데이터 마이그레이션 불필요.
 *
 * ─────────────────────────────────────────────────────────────
 * 정책 결정 기록 정책
 * ─────────────────────────────────────────────────────────────
 *
 * 이 함수가 호출되는 모든 차감 경로는 lib/credits/deduct.ts 를 통과한다.
 * 가격 정책이 바뀌면:
 *   1) 이 파일의 USD_PER_CREDIT 상수 수정
 *   2) 핸드오프 §9 (가격 정책) 섹션 업데이트
 *   3) 새 정책 적용 시점 명시 (소급 적용 안 함 — 기존 차감은 그대로)
 *
 * raw_cost_usd 는 항상 정확히 기록되므로 사후 분석·재정산 가능.
 */

/**
 * USD per credit. 1 크레딧 = 0.01 USD = 약 14원 (1 USD ≈ 1400원 기준).
 * 미래 정책 변경 시 이 상수만 수정.
 */
const USD_PER_CREDIT = 0.01;

/**
 * USD 비용 → 크레딧 정수.
 *
 * 항상 올림(`ceil`) — 사용자에게 유리한 방향이 아닌 *서비스에 유리한* 방향을 의식적으로 선택.
 * 이유: floor 또는 round로 하면 작은 호출이 0 크레딧으로 떨어져
 *       비용 누적 시 무료 호출 누수가 생김. 그렇다고 사용자에게 부당한 건 아님 —
 *       임시 정책이 워낙 후해서 (1 크레딧 = $0.01) 올림이 미치는 부담이 미미.
 *
 * 최소 1 크레딧 — 분류 1회가 0.0001 USD라도 1 크레딧으로 차감.
 * 이유: 호출 자체에 인프라 비용(DB 트랜잭션, 라우트 처리)이 있어 0은 부적절.
 */
export function usdToCredits(usd: number): number {
  if (usd <= 0) return 0;
  return Math.max(1, Math.ceil(usd / USD_PER_CREDIT));
}

/**
 * 크레딧 → 대략적 USD 표시. 디버깅/관리자 화면용.
 * 정확한 USD는 credit_transactions.raw_cost_usd 를 봐야 함.
 */
export function creditsToUsdApprox(credits: number): number {
  return credits * USD_PER_CREDIT;
}

/**
 * 분류 호출 전 잔액 체크용 최소 임계값.
 *
 * 잔액 ≥ 이 값이면 분류 호출 허용. 미만이면 즉시 차단.
 * "분류 1회 비용 추정"이 아니라 "분류 호출이 의미 있을 만한 최소 잔액"이라
 * 분류 실비보다 크게 잡음.
 *
 * M3 검증 단계 임시값 10. 페이지네이션·편집 비용 측정 후 조정.
 */
export const MIN_CREDIT_BALANCE_FOR_CLASSIFY = 10;
